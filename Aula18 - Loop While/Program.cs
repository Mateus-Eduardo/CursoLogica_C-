// See Loop WHILE / Estruturas de iteraão

using System;

class Aula18
{
	static void Main()
	{
		int contador = 0;
		while (contador < 5)
		{
			Console.WriteLine("Iteração " + contador);
			contador++;
		}

		Console.WriteLine("Fim do loop");

	}
}

// Loop while:
//O loop while é usado quando você não sabe quantas vezes precisará
//repetir uma ação, mas quer continuar enquanto uma condição for verdadeira.


